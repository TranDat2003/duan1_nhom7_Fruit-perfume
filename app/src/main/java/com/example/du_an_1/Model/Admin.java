package com.example.du_an_1.Model;

public class Admin {
    public int ma;
    public String username;
    public String mk;
    public String email;
    public String diachi;
    public String sdt;
    public String name;

    public Admin() {
    }

    public Admin(int ma, String username, String mk, String email, String diachi, String sdt, String name) {
        this.ma = ma;
        this.username = username;
        this.mk = mk;
        this.email = email;
        this.diachi = diachi;
        this.sdt = sdt;
        this.name = name;
    }
}
